# 짧은 문자열
# - 쌍따옴표(") 또는 따옴표(') 사용
print("1) Hello Python World!") 
print('2) Hello, Python World!')

# 긴 문자열(여러 줄로 이루어진 문자열)
# """ 또는 ''' 사용
print("""3) Hello,
Python
World!""")

print("""4) Hello,
    Python
    World!""")

print('''5) Hello,
Python!
World!''')

print('''6) Hello,
      
      
Python
      
World!''')