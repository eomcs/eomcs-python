# f-strings: 문자열 포맷팅

# f"문자열리터럴{표현식}문자열리터럴" 또는 F""
# 표현식? 
# - 값을 리턴하는 문장
# 
name = "홍길동"
print(name + "님 안녕하세요!") # f-strings 사용전
print(f"{name}님 안녕하세요!") # f"문자열"
print(F"{name}님 안녕하세요!") # F"문자열"
print(f'{name}님 안녕하세요!') # f'문자열'
print(F'{name}님 안녕하세요!') # F'문자열'

print(f"2 + 3 = {2 + 3}")

