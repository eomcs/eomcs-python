# 이스케이프 문자
# - 특별한 기능을 표현하는 문자 
# - 문자열 안에서 \로 시작한다.
print("Hello Python World!\
-Newline Ignored") # newline ignored
print("Hello Python World!\n-Linefeed") # Linefeed
print("Hello Python World!\r-CarriageReturn") # carriage return
print("Hello Python World!\f-Formfeed") # Form Feed
print("Hello Python World!\b-Backspace") # backspace
print("Hello Python World!\t-Horizontal Tab") # horizontal tab
print("Hello Python World!\v-Vertical Tab") # vertical tab
print("Hello 'Python' World!-Single Quote") # single quote
print("Hello \"Python\" World!-Double Quote") # double quote
print('Hello \'Python\' World!-Single Quote') # single quote
print('Hello "Python" World!-Double Quote') # double quote
print("Hello\\Python\\World!-Backspace") # backslash
print("Hello Python World!\a-Bell") # bell
